// MARK: - Storage

extension OpeningBook {
    #if DEBUG
    /// Test-only override for the books directory so unit tests don't touch real
    /// app data. Never set in production.
    nonisolated(unsafe) public static var _booksDirectoryOverride: URL?
    #endif

    /// `Application Support/<bundleID>/OpeningBooks/`. Large, re-downloadable
    /// assets belong here (not Documents), and the directory is excluded from
    /// iCloud backup by `ensureBooksDirectory()`.
    public static func booksDirectory() -> URL {
        #if DEBUG
        if let override = _booksDirectoryOverride { return override }
        #endif
        let fm = FileManager.default
        let base = fm.urls(for: .applicationSupportDirectory, in: .userDomainMask).first!
        let bundleID = Bundle.main.bundleIdentifier ?? "KataGoAnytime"
        return base
            .appendingPathComponent(bundleID, isDirectory: true)
            .appendingPathComponent("OpeningBooks", isDirectory: true)
    }

    /// Create the books directory if needed and mark it excluded-from-backup.
    /// `Downloader` does not create directories, so call this before downloading.
    @discardableResult
    public static func ensureBooksDirectory() throws -> URL {
        let dir = booksDirectory()
        let fm = FileManager.default
        if !fm.fileExists(atPath: dir.path) {
            try fm.createDirectory(at: dir, withIntermediateDirectories: true)
        }
        var values = URLResourceValues()
        values.isExcludedFromBackup = true
        var mutableDir = dir
        try? mutableDir.setResourceValues(values)
        return dir
    }

    /// The downloaded `.kbook.gz` archive on disk.
    public var downloadedURL: URL {
        OpeningBook.booksDirectory().appendingPathComponent(fileName)
    }

    public var isDownloaded: Bool {
        FileManager.default.fileExists(atPath: downloadedURL.path)
    }

    /// Size in bytes of the downloaded archive, or nil if not downloaded.
    public var onDiskSize: Int? {
        guard let attrs = try? FileManager.default.attributesOfItem(atPath: downloadedURL.path),
              let size = attrs[.size] as? Int else { return nil }
        return size
    }

    /// The decompressed `.kbook` file `BookLookup` writes to Caches, keyed by the
    /// archive name without its `.gz` extension. Removed alongside the archive on
    /// delete so a stale decompressed copy can't linger.
    public var decompressedCacheURL: URL {
        let cacheDir = FileManager.default.urls(for: .cachesDirectory, in: .userDomainMask).first!
        let name = downloadedURL.deletingPathExtension().lastPathComponent
        return cacheDir.appendingPathComponent(name)
    }

    /// Delete the downloaded archive and its decompressed cache file.
    public func deleteDownloaded() {
        let fm = FileManager.default
        try? fm.removeItem(at: downloadedURL)
        try? fm.removeItem(at: decompressedCacheURL)
    }
}
