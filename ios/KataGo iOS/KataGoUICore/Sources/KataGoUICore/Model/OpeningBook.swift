//  OpeningBook.swift
//  KataGo Anytime
//
//  Catalog + storage for the downloadable KataGo opening books (6x6...9x9,
//  Japanese-like variant). Mirrors `NeuralNetworkModel`, but downloaded books
//  live in Application Support (excluded from iCloud backup) rather than
//  Documents, because they are large and re-downloadable. The decompressed
//  `.kbook` cache stays in Caches (written by `BookLookup`).
//

import Foundation

public struct OpeningBook: Identifiable, Equatable, Sendable {
    public let id = UUID()
    public let boardSize: Int
    public let title: String
    public let description: String
    /// Download URL of the compact `.kbook.gz` (built from the katagobooks.org
    /// HTML book via scripts/build_book_db.py and published to a GitHub release).
    public let url: String
    public let fileName: String
    /// Compressed download size in bytes (the published books-v1 asset size),
    /// shown in the picker.
    public let fileSize: Int
    /// Komi the book was generated for (Japanese-like rules), informational.
    public let komi: Float

    public init(boardSize: Int,
                title: String,
                description: String,
                url: String,
                fileName: String,
                fileSize: Int,
                komi: Float) {
        self.boardSize = boardSize
        self.title = title
        self.description = description
        self.url = url
        self.fileName = fileName
        self.fileSize = fileSize
        self.komi = komi
    }

    public static func book(forBoardSize size: Int) -> OpeningBook? {
        allCases.first { $0.boardSize == size }
    }

